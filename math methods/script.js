// Math.floor() Method
// ye point k bad jo bhi number ho aus ko nai dekhta jo number hota hai wahi de deta hai or point bhui khatam kardeta hai. 
let floor1 = Math.floor(5.5) //5
let floor2 = Math.floor(5.2)//5
let floorc = Math.floor(-5.9)//-6

console.log(floor1, floor2, floorc);

// Math.ceil() Method 
// ye Method hamish aik number barha k deta hai or pont ko kahtam kar deta hai. 
let a = Math.ceil(5.5)//6
let b = Math.ceil(5.2)//6
let c = Math.ceil(-5.2)//-5

console.log(a, b, c);

// Math.round() Method 
// ye point k bad 5 se choti value ho to wahi number dedeta hai or agar point k bad 5 ho ya 5 se bara
//  yani 5 se 9 tak ho aik numbr barha k deta hai.
let round1 = Math.round(5.5)//6
let round2 = Math.round(5.2)//5
let roundc = Math.round(-5.9)//-6

console.log(round1,round2,roundc);

// Math.trunc() Method 
// point or point k bad jitne decimale ho sub ko remove kar deta hai or jo bhi number ho wohi deta hai.
let trunc1 = Math.trunc(5.5)//5
let trunc2 = Math.trunc(5.2)//5
let truncc = Math.trunc(-5.9)//-5

console.log(trunc1,trunc2,truncc);

